// Get URL parameters
function getUrlParameter(name) {
    name = name.replace(/[\[]/, '\\[').replace(/[\]]/, '\\]');
    const regex = new RegExp('[\\?&]' + name + '=([^&#]*)');
    const results = regex.exec(location.search);
    return results === null ? '' : decodeURIComponent(results[1].replace(/\+/g, ' '));
}

// Load article content
function loadArticle() {
    const articleId = parseInt(getUrlParameter('id'));
    const article = articlesData[articleId];
    
    if (!article) {
        // Redirect to home page if article not found
        window.location.href = 'index.html';
        return;
    }
    
    // Update page title
    document.getElementById('articleTitle').textContent = `${article.title} - PostHaven`;
    
    // Update breadcrumb
    document.getElementById('breadcrumbCategory').textContent = article.category.charAt(0).toUpperCase() + article.category.slice(1);
    
    // Update article header
    document.getElementById('articleCategory').textContent = article.category.toUpperCase();
    document.getElementById('articleDetailTitle').textContent = article.title;
    document.getElementById('authorName').textContent = article.author;
    document.getElementById('dateText').textContent = formatDate(article.date);
    document.getElementById('readTimeText').textContent = article.readTime;
    
    // Update article image
    const articleImage = document.getElementById('articleDetailImage');
    articleImage.src = article.image;
    articleImage.alt = article.title;
    
    // Update article content
    document.getElementById('articleFullContent').innerHTML = article.content;
    
    // Update tags
    const tagsContainer = document.getElementById('articleTags');
    tagsContainer.innerHTML = article.tags.map(tag => 
        `<span class="tag">${tag}</span>`
    ).join('');
    
    // Setup share functionality
    setupShareButtons(article);
    
    // Load related articles
    loadRelatedArticles(article.category, article.id);
    
    // Setup navigation
    setupArticleNavigation(article.id);
}

// Format date for display
function formatDate(dateString) {
    const options = { year: 'numeric', month: 'long', day: 'numeric' };
    return new Date(dateString).toLocaleDateString('en-US', options);
}

