// Setup article navigation (previous/next)
function setupArticleNavigation(currentId) {
    const allArticles = Object.values(articlesData).sort((a, b) => new Date(b.date) - new Date(a.date));
    const currentIndex = allArticles.findIndex(article => article.id === currentId);
    
    if (currentIndex > 0) {
        const prevArticle = allArticles[currentIndex - 1];
        const prevElement = document.getElementById('prevArticle');
        prevElement.style.display = 'block';
        prevElement.querySelector('.nav-title').textContent = prevArticle.title;
        prevElement.onclick = () => navigateToArticle(prevArticle.id);
    }
    
    if (currentIndex < allArticles.length - 1) {
        const nextArticle = allArticles[currentIndex + 1];
        const nextElement = document.getElementById('nextArticle');
        nextElement.style.display = 'block';
        nextElement.querySelector('.nav-title').textContent = nextArticle.title;
        nextElement.onclick = () => navigateToArticle(nextArticle.id);
    }
}

