// Setup reading progress indicator
function setupReadingProgress() {
    const progressFill = document.querySelector('.reading-progress-fill');
    const articleContent = document.getElementById('articleFullContent');
    
    if (!articleContent) return;
    
    window.addEventListener('scroll', function() {
        const articleStart = articleContent.offsetTop;
        const articleHeight = articleContent.offsetHeight;
        const windowHeight = window.innerHeight;
        const scrollPosition = window.pageYOffset;
        
        // Calculate reading progress
        const startReading = articleStart - windowHeight + 100;
        const finishReading = articleStart + articleHeight;
        
        if (scrollPosition >= startReading && scrollPosition <= finishReading) {
            const progress = (scrollPosition - startReading) / (finishReading - startReading);
            const progressPercentage = Math.min(Math.max(progress * 100, 0), 100);
            progressFill.style.width = progressPercentage + '%';
        } else if (scrollPosition < startReading) {
            progressFill.style.width = '0%';
        } else {
            progressFill.style.width = '100%';
        }
    });
}

