// Load related articles
function loadRelatedArticles(category, currentId) {
    const relatedContainer = document.getElementById('relatedArticles');
    const relatedArticles = Object.values(articlesData)
        .filter(article => article.category === category && article.id !== currentId)
        .slice(0, 3);
    
    if (relatedArticles.length === 0) {
        // If no articles in same category, show random articles
        const otherArticles = Object.values(articlesData)
            .filter(article => article.id !== currentId)
            .slice(0, 3);
        relatedContainer.innerHTML = otherArticles.map(createRelatedArticleHTML).join('');
    } else {
        relatedContainer.innerHTML = relatedArticles.map(createRelatedArticleHTML).join('');
    }
}

